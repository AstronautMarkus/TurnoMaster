<script>
function togglePasswordVisibility() {
    var passwordFields = ['password', 'password-confirm'];
    passwordFields.forEach(function(id) {
        var input = document.getElementById(id);
        if (input.type === "password") {
            input.type = "text";
        } else {
            input.type = "password";
        }
    });
}
